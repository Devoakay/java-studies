public class ShipCoordinates
{
    private int row;
    private int column;

    public ShipCoordinates(int row, int column)
    {
        this.row = row;
        this.column = column;
    }
    // Getters
    public int getRow() {
        return row;
    }
    public int getColumn() {
        return column;
    }
}
